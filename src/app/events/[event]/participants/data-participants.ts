export async function setSelectedParticipants(ids: string[]) {
  participants.filter(p => p.gonnaPlay).forEach(p => { p.gonnaPlay = false })

  ids.forEach(id => {
    const participant = participants.find(p => p.id === id)
    if (!participant) {
      throw new Error(`Usuário não encontrado: ${id}`)
    }
    participant.gonnaPlay = true
  })
}

export async function checkParticipant(id: string) {
  const participant = participants.find(p => p.id === id)
  if (!participant) {
    throw new Error('Participante não encontrado')
  }
  participant.wannaPlay = !participant.wannaPlay
}
